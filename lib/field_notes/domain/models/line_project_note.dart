class LineProjectNote {
  const LineProjectNote({
    required this.lineCallsign,
    required this.gradeConversation,
    required this.wallPositionMemory,
    required this.clippedBoltCount,
    required this.asksForSmallRack,
    required this.cruxShape,
    required this.restPocketHint,
    required this.sessionUse,
  });

  final String lineCallsign;
  final String gradeConversation;
  final String wallPositionMemory;
  final int clippedBoltCount;
  final bool asksForSmallRack;
  final String cruxShape;
  final String restPocketHint;
  final String sessionUse;
}
